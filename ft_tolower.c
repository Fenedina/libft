#include "libft.h"
int	ft_tolower(int nbr)
{
	int	i;

	i = 0;
	if (nbr >= 65 && nbr <= 90)
	{
		nbr = nbr - 'A' + 'a';
		i++;
	}
	return (nbr);
}
