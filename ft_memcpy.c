#include "libft.h"
void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*A;
	char	*B;

	i = 0;
	A = (char *) dest;
	B = (char *) src;
	if (!n || dest == src)
		return (dest);
	while (i < n)
	{
		A[i] = B[i];
		i++;
	}
	if (i < n)
	{
		A[i] = 0;
	}
	return (A);
}
