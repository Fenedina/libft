#include "libft.h"
int	ft_toupper(int nbr)
{
	int	i;

	i = 0;
	if (nbr >= 97 && nbr <= 122)
	{
		nbr = nbr - 'a' + 'A';
		i++;
	}
	return (nbr);
}
