#include "libft.h"
void	ft_bzero(void *dest, size_t kol)
{
	size_t	i;
	int		c;
	char	*A;

	i = 0;
	c = '\0';
	A = (char *) dest;
	while (i < kol)
	{
		A[i] = c;
		i++;
	}
	return (*dest);
}
